package models;

public class FileDiff {
	private String fileName;
	
	private String diff;
	
	public String getFileName() {
		return fileName;
	}
	
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	
	public String getDiff() {
		return diff;
	}
	
	public void setDiff(String diff) {
		this.diff = diff;
	}
}
